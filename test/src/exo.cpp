#include "exo.h"

double MyFirstLib::MyMathClass::Add(double a, double b)
{
    return a + b;
}

double MyFirstLib::MyMathClass::Sub(double a, double b)
{
    return a - b;
}

double MyFirstLib::MyMathClass::Mul(double a, double b)
{
    return a * b;
}

double MyFirstLib::MyMathClass::Div(double a, double b)
{
    return a / b;
}
